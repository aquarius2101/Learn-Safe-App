    private void submitBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBTNActionPerformed
        // Calculate grades
        grade();

        // update user's fire score
        loggedInUser.setFireScore(Integer.toString(fireScore));
        
        // reading data from file to array list
        users = AccountManager.load();

        //update in the array list by replacing old account with updated account
        for (int i = 0; i < users.size(); i++) {
            if (users.get(i).getUsername().equals(loggedInUser.getUsername())) {
                users.set(i, loggedInUser); // replace with updated account
                break;
            }
        }

        // Write array list back to file
        AccountManager.writeToFile(users);

        // Display test score when going back to main program
        MainProgramGUI m = new MainProgramGUI(loggedInUser, users);
        m.displayFireScore();
        m.setVisible(true);
        dispose();
    }//GEN-LAST:event_submitBTNActionPerformed

