    private int fireScore = 0;
    @Override
    public void grade() {
        //Question 1
        question1 = false;
        if (q1bCB.isSelected() && q1dCB.isSelected()) {    //Correct answers are B and D
            question1 = true;
        }
        if (q1aCB.isSelected() || q1cCB.isSelected()) {
            question1 = false;
        }
        if (question1 == true) {
            fireScore++;
        }

        //Question 2: Correct answer is A
        if (q2aRBTN.isSelected()) {
            fireScore++;
        }

        //Question 3: Correct answers are B, C, and E
        question3 = false;
        if (q3bCB.isSelected() & q3cCB.isSelected() & q3eCB.isSelected()) {
            question3 = true;
        }
        if (q3aCB.isSelected() || q3dCB.isSelected()) {
            question3 = false;
        }
        if (question3 == true) {
            fireScore++;
        }

        //Question 4: Correct answer is B
        if (q4bRBTN.isSelected()) {
            fireScore++;
        }

        //Question 5: Correct answer is D
        if (q5dRBTN.isSelected()) {
            fireScore++;
        }
    }
